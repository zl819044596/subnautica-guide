export function getCreatureBySlug(slug: string): Creature | undefined {
  return creatures.find((c) => c.slug === slug);
}

export function getAllSlugs(): string[] {
  return creatures.map((c) => c.slug);
}

export function getCreaturesByCategory(category: Creature["category"]): Creature[] {
  return creatures.filter((c) => c.category === category);
}

export function getCreaturesByBiome(biome: string): Creature[] {
  return creatures.filter((c) => c.biomes.includes(biome));
}

export function getEdibleCreatures(): Creature[] {
  return creatures.filter((c) => c.edible);
}

export function getDangerousCreatures(): Creature[] {
  return creatures.filter((c) => c.dangerLevel === "dangerous" || c.dangerLevel === "extreme");
}
